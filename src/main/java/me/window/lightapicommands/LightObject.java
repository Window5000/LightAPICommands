package me.window.lightapicommands;

public class LightObject {
    public int x, y, z, level;
    public String world;

    public LightObject(int x, int y, int z, String world, int level) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.level = level;
        this.world = world;
    }
}
